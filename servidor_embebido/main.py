#Función para conexión de red
def conectar_wifi(wdt):
    print("[WIFI] Iniciando conexion a red...")

    # MODIFICACION WDT (PRACTICA RED):
    # Se alimenta el watchdog antes de iniciar la secuencia WiFi para arrancar en estado sano.
    wdt.feed()

    wlan = network.WLAN(network.STA_IF)

    # MODIFICACION WDT (PRACTICA RED):
    # Se alimenta otra vez antes de activar la interfaz, por seguridad en esta etapa critica.
    wdt.feed()
    wlan.active(True)

    # MODIFICACION WDT (PRACTICA RED):
    # Si connect() se llegara a bloquear por un fallo interno de red, el watchdog no recibira feed
    # y forzara un reinicio automatico en ~8 segundos.
    wlan.connect(SSID, PASSWORD)

    print("[WIFI] Conectando a WiFi...", end="")
    ultimo_log_wifi = utime.ticks_ms()
    while not wlan.isconnected():
        # MODIFICACION LOG SERIAL:
        # Se imprime un punto cada ciclo para ver visualmente que sigue intentando conectar.
        print(".", end="")
        utime.sleep(0.3)

        # MODIFICACION LOG SERIAL:
        # Cada segundo se escribe un mensaje de estado en el monitor serial.
        # Importante: aqui NO se alimenta el watchdog, para que un fallo prolongado reinicie el ESP32.
        if utime.ticks_diff(utime.ticks_ms(), ultimo_log_wifi) >= WIFI_LOG_INTERVAL_MS:
            print("\n[WIFI] Aun esperando conexion...")
            ultimo_log_wifi = utime.ticks_ms()

    # MODIFICACION WDT (PRACTICA RED):
    # Se alimenta al salir del while para dejar margen de tiempo al resto de inicializaciones.
    wdt.feed()
    print("\n[WIFI] WiFi conectado")
    print("[WIFI] IP del ESP32:", wlan.ifconfig()[0])
    return wlan

#Función para creación de socket UDP
def iniciar_udp():
    addr = socket.getaddrinfo("0.0.0.0", UDP_PORT)[0][-1]
    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    sock.bind(addr)
    sock.settimeout(0.1)  # no bloqueante por mucho tiempo
    print("[UDP] Servidor escuchando en puerto", UDP_PORT)
    return sock

# Función para crear socket multicast
def iniciar_multicast():
    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    return sock

# Función para enviar datos por multicast
def enviar_multicast(sock, mensaje):
    sock.sendto(mensaje.encode(), (MULTICAST_GROUP, MULTICAST_PORT))
#Función para procesar mensaje recibido por UNICAST
def procesar_mensaje(mensaje):
    mensaje = mensaje.strip().upper()

    if mensaje == "LED_ON":
        led.value(1)
        return "ACK:LED_ON"

    elif mensaje == "LED_OFF":
        led.value(0)
        return "ACK:LED_OFF"

    elif mensaje == "STATUS":
        estado = "ON" if led.value() else "OFF"
        return "STATUS:LED={}".format(estado)

    else:
        return "ERROR:COMANDO_DESCONOCIDO"

#Función para obtención de temperatura y humedad DHT22
def obtener_temperatura():
    sensor.measure()
    temp = sensor.temperature()
    hum = sensor.humidity()
    return temp, hum

#Funcion para mostrar info en pantalla
def mostrar_emoji(temp, hum):
    oled.fill(0)
    oled.text("Temp: {:.1f}C".format(temp), 0, 0)
    oled.text("Hum: {:.1f}%".format(hum), 0, 10)

    if temp < 15:
        oled.text("FRIO [*]", 30, 30)
    elif temp >= 15 and temp <= 30:
        oled.text("NORMAL :)", 30, 30)
    else:
        oled.text("CALOR [!]", 30, 30)

    estado_led = "ON" if led.value() else "OFF"
    oled.text("LED: {}".format(estado_led), 0, 50)

    oled.show()

