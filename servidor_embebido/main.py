# MODIFICACION WDT (PRACTICA RED):
# Se crea el watchdog de hardware con una ventana de 8 segundos.
# NOTA: En ESP32, una vez creado, este watchdog queda activo y debe alimentarse de forma periodica.
wdt = machine.WDT(timeout=WDT_TIMEOUT_MS)
print("[BOOT] Watchdog activado con timeout de {} ms".format(WDT_TIMEOUT_MS))

# MODIFICACION WDT (PRACTICA RED):
# Se pasa el watchdog a la funcion WiFi para proteger esta fase de posibles bloqueos.
wlan = conectar_wifi(wdt)
sock = iniciar_udp()
sock_multi = iniciar_multicast()
print("[BOOT] Inicializacion principal completada")

ultimo_update = utime.ticks_ms()
intervalo_display = 2000  # ms

while True:
    # MODIFICACION WDT (PRACTICA RED):
    # "Seguro de vida" general: cada iteracion del loop principal alimenta el watchdog.
    # Si el codigo se congela y deja de ejecutar esta linea por >8 s, el hardware reinicia el ESP32.
    wdt.feed()

    # 1. Revisar si llegó un mensaje UDP
    try:
        data, remote_addr = sock.recvfrom(1024)
        mensaje = data.decode("utf-8")
        print("[UDP] Mensaje recibido desde", remote_addr, "->", mensaje)

        respuesta = procesar_mensaje(mensaje)
        sock.sendto(respuesta.encode("utf-8"), remote_addr)
        print("[UDP] Respuesta enviada ->", respuesta)

    except OSError:
        pass  # no llegó nada, continuamos

    # 2. Actualizar pantalla/sensor cada 2 segundos
    if utime.ticks_diff(utime.ticks_ms(), ultimo_update) >= intervalo_display:
        try:
            temperatura, humedad = obtener_temperatura()
            print("[SENSOR] Temp: {}C, Hum: {}%".format(temperatura, humedad))
            mostrar_emoji(temperatura, humedad)
            # MODIFICACION ESCALABILIDAD (GRANJA DE SENSORES):
            # El multicast ahora incluye el ID unico del dispositivo para auto-registro en gateway.
            mensaje = "ID={};EVENT:TEMP={};HUM={}".format(DEVICE_ID, temperatura, humedad)
            enviar_multicast(sock_multi, mensaje)
            print("[MULTICAST] Enviado: " + mensaje)
        except OSError:
            print("[SENSOR] Error al leer el sensor DHT")
            oled.fill(0)
            oled.text("Error Sensor!", 20, 20)
            oled.text("LED: {}".format("ON" if led.value() else "OFF"), 20, 40)
            oled.show()

        ultimo_update = utime.ticks_ms()